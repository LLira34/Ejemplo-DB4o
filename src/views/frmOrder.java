    public void selected() {
        tblOrders.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent evt) {
                if (evt.getClickCount() == 1) {
                    txtOrderID.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 0).toString());
                    txtUpOrderID.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 0).toString());
                    txtCustomerID.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 1).toString());
                    txtEmployeeID.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 2).toString());
                    txtOrderDate.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 3).toString());
                    txtRequiredDate.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 4).toString());
                    txtShippedDate.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 5).toString());
                    txtUpShipVia.setText(tblOrders.getValueAt(tblOrders.getSelectedRow(), 6).toString());
                }
            }
        });
    }

