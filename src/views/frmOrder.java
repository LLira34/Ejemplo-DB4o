    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            DateFormat fecha = new SimpleDateFormat("dd-MM-yyyy");

            int orderID = Integer.parseInt(txtOrderID.getText());
            int customerID = Integer.parseInt((String) cmbCustomerID.getSelectedItem());
            int employeeID = Integer.parseInt((String) cmbEmployeeID.getSelectedItem());
            Date od1 = new Date();
            od1 = dcsOrderDate.getDate();
            String orderDate = fecha.format(od1);

            System.out.println(orderDate);
            Date rd1 = new Date();
            rd1 = dcsRequiredDate.getDate();
            String requiredDate = fecha.format(rd1);

            Date sd1 = new Date();
            sd1 = dcsShippedDate.getDate();
            String shippedDate = fecha.format(sd1);
            int shipVia = Integer.parseInt(txtShipVia.getText());

            boolean insert = this.orderController.save(orderID, customerID, employeeID, orderDate, requiredDate, shippedDate, shipVia);
            if (insert == true) {
                JOptionPane.showMessageDialog(null, "Datos guardados");
                this.clean();
                tblOrders.setModel(this.orderController.orders());
            } else {
                JOptionPane.showMessageDialog(null, "Falló al insertar");
            }
        } catch (NumberFormatException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "El ID introducido no es numérico");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

