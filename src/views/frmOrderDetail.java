    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtId.getText());
            int orderId = Integer.parseInt((String) cmbOrder.getSelectedItem());
            int productID = Integer.parseInt((String) cmbProduct.getSelectedItem());
            double unitPrice = Double.parseDouble(txtUnitPrice.getText());
            int quantity = Integer.parseInt(txtQuantity.getText());
            double discount = Double.parseDouble(txtDiscount.getText());
            boolean insert = this.odc.save(id, orderId, productID, unitPrice, quantity, discount);
            if (insert == true) {
                JOptionPane.showMessageDialog(null, "Datos guardados");
                this.clean();
                tblOrderDetails.setModel(this.odc.details());
            } else {
                JOptionPane.showMessageDialog(null, "FallÃ³ al insertar");
            }
        } catch (NumberFormatException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "El ID introducido no es numÃ©rico");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

